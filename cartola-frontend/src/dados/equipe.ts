export const ordenarEquipes = (equipes: Equipe[]): Equipe[] => {
  return [...equipes].sort((a, b) => { // Cria uma cópia para não modificar o array original
    if (b.pontos !== a.pontos) {
      return b.pontos - a.pontos; // Ordena por pontos (descendente)
    }
    if (b.vitorias !== a.vitorias) {
      return b.vitorias - a.vitorias; // Ordena por vitórias (descendente)
    }
    if (b.empates !== a.empates) {
      return b.empates - a.empates; // Ordena por empates (descendente)
    }
    if (a.derrotas !== b.derrotas) {
        return a.derrotas - b.derrotas; // Ordena por derrotas (ascendente, ou seja, menos derrotas primeiro)
    }
    return b.saldoPontos - a.saldoPontos; // Ordena por saldo de pontos (descendente)
  });
};

// Exemplo de uso:
const equipesOrdenadas = ordenarEquipes(equipes);

